async function main() {
  console.log('Seeding vehicle models...');

  // 获取所有租户
  const tenants = await prisma.tenant.findMany({ where: { status: 'active' } });

  if (tenants.length === 0) {
    console.log('No tenants found, skipping vehicle model seed');
    return;
  }

  for (const tenant of tenants) {
    console.log(`Seeding for tenant: ${tenant.name} (${tenant.id})`);

    // 获取该租户的客户
    const customers = await prisma.customer.findMany({
      where: { tenantId: tenant.id, status: 'active' },
      take: 1,
    });

    if (customers.length === 0) {
      console.log('  No customers found, skipping');
      continue;
    }

    const customerId = customers[0].id;
    let count = 0;

    for (const [brand, seriesMap] of Object.entries(VEHICLE_MODELS)) {
      for (const [series, yearMap] of Object.entries(seriesMap)) {
        for (const [year, models] of Object.entries(yearMap)) {
          for (const detail of models) {
            const modelStr = `${brand}/${series}/${year}/${detail}`;
            const plateNo = `京${String.fromCharCode(65 + Math.floor(Math.random() * 26))}${String(Math.floor(Math.random() * 10000)).padStart(4, '0')}`;

            // 检查是否已存在相同车型
            const existing = await prisma.vehicle.findFirst({
              where: { tenantId: tenant.id, model: modelStr },
            });

            if (!existing) {
              await prisma.vehicle.create({
                data: {
                  tenantId: tenant.id,
                  customerId,
                  plateNo,
                  model: modelStr,
                  brand,
                  color: ['白色', '黑色', '银色', '灰色', '红色', '蓝色'][Math.floor(Math.random() * 6)],
                },
              });
              count++;
            }
          }
        }
      }
    }

    console.log(`  Created ${count} vehicle models`);
  }

  console.log('Vehicle model seed completed!');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
